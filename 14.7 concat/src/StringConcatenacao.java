
public class StringConcatenacao {

	public static void main(String[] args) {
		
		String s1 = "Happy ";
		String s2 = "Birthday";
		
		System.out.printf("s1 = %s%ns2 = %s%n", s1, s2);
		System.out.printf("Resultado da concatenação s1.concat(s2) = %s%n", s1.concat(s2));
		System.out.printf("s1 depois da concatenação = %s%n", s1);
		
	}

}
